/*
**********************************************************************************
*函数名称：void MEM_cpy(Uint8 * dest,Uint8 * src,Uint8 count)                    
*函数功能：由src所指内存区域复制count个字节到dest所指内存区域                    
*入口参数：dest 目标地址                                                         
*          src 源地址                                                            
*          Uint8 count 复制长度                                                  
*返回参数：无                                                                    
**********************************************************************************
*/
void MEM_Cpy(Uint8 * dest,Uint8 * src,Uint8 count)
{
	while(count != 0){
		* dest = * src;
		dest ++;
		src ++;
		count --;
	}
}

/*
**********************************************************************************
*函数名称：Bit MEM_cmp(Uint8 * buf1,Uint8 * buf2,Uint8 count)                     
*函数功能：比较内存区域buf1和buf2的前count个字节是否相同                          
*入口参数：buf1 buf2 count                                                        
*返回参数：相等为0 不等为1                                                        
**********************************************************************************
*/
Bool MEM_Cmp(Uint8 * buf1,Uint8 * buf2,Uint8 count)
{
	while(count != 0){
		if(* buf1 != * buf2)
			return 1;
		buf1 ++;
		buf2 ++;
		count --;
	}
	return 0;
}


/*
**********************************************************************************
*函数名称：MEM_set(Uint8 *buffer,Uint8 c,Uint8 count)                             
*函数功能：把buffer所指内存区域的前count个字节设置成字符c。                       
*入口参数：buffer c count                                                         
*返回参数：无                                                                     
**********************************************************************************
*/
void MEM_Set(Uint8 *buffer,Uint8 c,Uint16 count)
{
	while(count !=0){
		* buffer = c;
		buffer ++;
		count --;
	}
}

